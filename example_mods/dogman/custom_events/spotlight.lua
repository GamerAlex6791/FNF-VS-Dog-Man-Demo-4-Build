function onCreatePost()
	makeLuaSprite("blackbg", "SpotlightdarkBG", 0, 0)
	setObjectCamera("blackbg", "other")
	setProperty("blackbg.alpha", 0.7)
	scaleObject("blackbg", 1, 1)
	setBlendMode("blackbg", "overlay")
	addLuaSprite("blackbg", true)
	makeLuaSprite("spot", "newfoverlay/dadspot", 0, 0)
	setObjectCamera("spot", "other")
	setProperty("spot.alpha", 0.7)
	scaleObject("spot", 1, 1)
	setBlendMode("spot", "add")
	addLuaSprite("spot", true)
end